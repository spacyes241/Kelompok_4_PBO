    private void listJadwalTersediaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_listJadwalTersediaMouseClicked
        editButton.setVisible(true);
        deleteButton.setVisible(true);
        String selectedNama = listJadwalTersedia.getSelectedValue();
        if (selectedNama != null) {
            try {
                String query = "SELECT * FROM dbjadwal WHERE name = ?";
                PreparedStatement ps = conn.prepareStatement(query);
                ps.setString(1, selectedNama);
                ResultSet rs = ps.executeQuery();
                if (rs.next()) {
                    namaFill.setText(rs.getString("name"));
                    pelatihFill.setText(rs.getString("trainer"));
                    tingkatFill.setText(rs.getString("difficulty"));
                    durasiFill.setText(rs.getString("duration"));
                    jamFill.setText(rs.getString("startTime"));
                    selectedJadwalTersedia = rs.getString("name");
                }
            } catch (SQLException e) {
                e.getMessage();
            }
        }
    }//GEN-LAST:event_listJadwalTersediaMouseClicked

